# Integers
# You can add (+), subtract (-), multiply (*), and divide (/) integers in Python.
sum = 3 + 2
print(sum)

difference = 3 - 2
print(difference)

product = 3 * 2
print(product)

division = 3 / 2
print(division)

# Python uses two multiplication symbols to represent exponents:
square_of_3 = 3 ** 2
print(square_of_3)

# Modulo used to find the remainder
remainder = 3 % 2
print(remainder)

# type: ignore # Floats: Python calls any number with a decimal point a float.
decimal_sum = 2.1 + 0.1
print(decimal_sum)

decimal_difference = 2.1 - 0.1
print(decimal_difference)

decimal_product = 2.1 * 0.1
print(decimal_product)

decimal_division = 2.1 / 0.1
print(decimal_division)